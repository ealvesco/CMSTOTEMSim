//----------------------------------------------------------------------------------------------------

void TotemRPGeometryLite::BuildMeanRPData()
{
  rpData.clear();

  // make sums
  for (auto sit : sensorData)
  {
    const unsigned int &senId = sit.first;
    unsigned int rpId = senId / 10;
    bool uDirection = (senId % 2 == 1);

    auto rit = rpData.find(rpId);
    if (rit == rpData.end())
      rpData[rpId] = {0, 0, 0., 0., 0., 0., 0., 0., 0.};

    RPGeomInfo &rpd = rpData[rpId];

    rpd.x += sit.second.x;
    rpd.y += sit.second.y;
    rpd.z += sit.second.z;

    if (uDirection)
    {
      rpd.n_U++;
      rpd.dx_U += sit.second.dx;
      rpd.dy_U += sit.second.dy;
    } else {
      rpd.n_V++;
      rpd.dx_V += sit.second.dx;
      rpd.dy_V += sit.second.dy;
    }
  }

  // normalise
  for (auto &rit : rpData)
  {
    RPGeomInfo &rpd = rit.second;
    unsigned int n = rpd.n_U + rpd.n_V;

    rpd.x = (n > 0) ? rpd.x / n : 0.;
    rpd.y = (n > 0) ? rpd.y / n : 0.;
    rpd.z = (n > 0) ? rpd.z / n : 0.;

    rpd.dx_U = (rpd.n_U > 0) ? rpd.dx_U / rpd.n_U : 0.;
    rpd.dy_U = (rpd.n_U > 0) ? rpd.dy_U / rpd.n_U : 0.;
    rpd.dx_V = (rpd.n_V > 0) ? rpd.dx_V / rpd.n_V : 0.;
    rpd.dy_V = (rpd.n_V > 0) ? rpd.dy_V / rpd.n_V : 0.;
  }
}

